package simpleExercises.Strings;

/**
 * Write a Java program to get the last index of a string within a string.
 Napisz program w języku Java, aby uzyskać ostatni indeks ciągu w ciągu znaków.
 */
public class String21 {
    public static void main(String[] args) {

        String string = "The quick brown fox jumps over the lazy dog.";

        int a = string.lastIndexOf("a", string.length() - 1);
        int b = string.lastIndexOf("b", string.length() - 1);
        int c = string.lastIndexOf("c", string.length() - 1);
        int d = string.lastIndexOf("d", string.length() - 1);
        int e = string.lastIndexOf("e", string.length() - 1);
        int f = string.lastIndexOf("f", string.length() - 1);
        int g = string.lastIndexOf("g", string.length() - 1);
        int h = string.lastIndexOf("h", string.length() - 1);
        int i = string.lastIndexOf("i", string.length() - 1);
        int j = string.lastIndexOf("j", string.length() - 1);
        int k = string.lastIndexOf("k", string.length() - 1);
        int l = string.lastIndexOf("l", string.length() - 1);
        int m = string.lastIndexOf("m", string.length() - 1);
        int n = string.lastIndexOf("n", string.length() - 1);
        int o = string.lastIndexOf("o", string.length() - 1);
        int p = string.lastIndexOf("p", string.length() - 1);
        int q = string.lastIndexOf("q", string.length() - 1);
        int r = string.lastIndexOf("r", string.length() - 1);
        int s = string.lastIndexOf("s", string.length() - 1);
        int t = string.lastIndexOf("t", string.length() - 1);
        int u = string.lastIndexOf("u", string.length() - 1);
        int v = string.lastIndexOf("v", string.length() - 1);
        int w = string.lastIndexOf("w", string.length() - 1);
        int x = string.lastIndexOf("x", string.length() - 1);
        int y = string.lastIndexOf("y", string.length() - 1);
        int z = string.lastIndexOf("z", string.length() - 1);

        // Display the results of all the lastIndexOf method calls.
        System.out.println(" a  b c  d  e  f  g  h i  j");
        System.out.println("===========================");
        System.out.println(a + " " + b + " " + c + " " + d + " " +
                e + " " + f + " " + g + " " + h + " " +
                i + " " + j + "\n");

        System.out.println("k  l  m  n  o  p q  r  s  t");
        System.out.println("===========================");
        System.out.println(k + " " + l + " " + m + " " + n + " " +
                o + " " + p + " " + q + " " + r + " " +
                s + " " + t + "\n");

        System.out.println(" u  v  w  x  y  z");
        System.out.println("=================");
        System.out.println(u + " " + v + " " + w + " " + x + " " +
                y + " " + z);
    }

}

